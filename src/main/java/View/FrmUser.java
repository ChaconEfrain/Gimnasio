    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        
        userModel model = new userModel();
        String name = txt_nombre.getText();
        model.setName(name);
        String apellido = txt_apellido.getText();
        model.setApellido(apellido);
        java.util.Date fechaNacimiento = dt_fechaDeNacimiento.getDate();
        model.setFechaNacimiento(fechaNacimiento);
        String opcion = cb_tipoDeUsuario.getSelectedItem().toString();
        int tipoUsuario = 0;
        switch(opcion){
            case "Cliente":
                tipoUsuario = 1;
                break;
            case "Trabajador":
                tipoUsuario = 2;
                break;
        }
        model.setTipoDeUsuario(tipoUsuario);
        String informacion = txt_informacion.getText();
        model.setInfoExtra(informacion);
        char[] pass = txt_pass.getPassword();
        String str = new String(pass);
        model.setPassword(str);
        user controller = new user();
        try {
            if(txt_id.getText().isEmpty()){
            controller.insertUser(model);
            this.setVisible(false);
            }else{         
                int id = Integer.parseInt(txt_id.getText());
                model.setId(id);
                controller.update(model);
                this.setVisible(false);
            }
            FrmUserTable userTable = new FrmUserTable();
            userTable.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(FrmUser.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }//GEN-LAST:event_btn_guardarActionPerformed

