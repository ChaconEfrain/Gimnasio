    private void tbl_datosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_datosMouseClicked
        int seleccionar = tbl_datos.rowAtPoint(evt.getPoint());
        txt_ID.setText(String.valueOf(tbl_datos.getValueAt(seleccionar, 0)));
        txt_name.setText(String.valueOf(tbl_datos.getValueAt(seleccionar, 1)));
        
        if(!txt_ID.getText().isEmpty() && !txt_name.getText().isEmpty()){
            btn_editar.setEnabled(true);
            btn_eliminar.setEnabled(true);
            btn_agregar.setEnabled(false);
        }else{
            btn_editar.setEnabled(false);
            btn_eliminar.setEnabled(false);
            btn_agregar.setEnabled(true);
        }
    }//GEN-LAST:event_tbl_datosMouseClicked

