    public void tableFill() throws SQLException{
        DefaultTableModel tableModel = new DefaultTableModel();
        user userController = new user();
//        Object[] columns = new Object[7];
        ArrayList<userModel> list = userController.usersData();
        Object[] columnsName = new Object[7];
        
        columnsName[0] = "Id";
        columnsName[1] = "Name";
        columnsName[2] = "Last name";
        columnsName[3] = "Birth date";
        columnsName[4] = "Info";
        columnsName[5] = "User type";
        columnsName[6] = "Password";
        tableModel.setColumnIdentifiers(columnsName);

        
        Object[] rowData = new Object[7];
        
        for(int i = 0; i < list.size(); i++){
            
            rowData[0] = list.get(i).getId();
            rowData[1] = list.get(i).getName();
            rowData[2] = list.get(i).getApellido();
            rowData[3] = list.get(i).getFechaNacimiento();
            rowData[4] = list.get(i).getInfoExtra();
            rowData[5] = list.get(i).getTipoDeUsuario();
            rowData[6] = list.get(i).getPassword();
               
               tableModel.addRow(rowData);
        }
        tbl_datos.setModel(tableModel);
    }

